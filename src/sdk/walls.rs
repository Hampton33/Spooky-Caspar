pub fn is_point_in_wall(point: &nc::na::Point2<f32>) -> bool {
    let start_time = std::time::Instant::now();
    let (row_position, col_position) = (point.y / 15000.0 * 5.0, point.x / 15000.0 * 5.0);
    let (row_index, col_index) = ((5.0 - row_position).floor() as usize, col_position.floor() as usize);

    // Check if chunk position is out of bounds
    if row_index >= 5 || col_index >= 5 {
        println!("Chunk position is out of bounds.");
        return false;
    }

    let (tile_row, tile_col) = (
        (20.0 - (row_position % 1.0 * 20.0)).floor() as usize,
        (col_position % 1.0 * 20.0).floor() as usize,
    );

    // Check if tile position is out of bounds
    if tile_row >= 20 || tile_col >= 20 {
        println!("Tile position is out of bounds.");
        return false;
    }

    let tile_value = MAP_CHUNKS[row_index][col_index].rows[tile_row][tile_col];
    println!("Checked point, time it took: {:?}", start_time.elapsed());
    return tile_value == 1;
}

